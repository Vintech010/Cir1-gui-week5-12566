void main (){
  print("Assignment operator");
  print("============");
  const int n1 = 10;
  const int n2 = 5;
  print("$n1 + $n2 = ${n1 + n2}");//ADD บวก/เพิ่ม
  print("$n1 - $n2 = ${n1 - n2}");//Substraction ลบ
  print("$n1 * $n2 = ${n1 * n2}");//Multication คูณ
  print("$n1 / $n2 = ${n1 / n2}");//Division หาร
  print("$n1 % $n2 = ${n1 % n2}");//Modulus หาเศษ
}